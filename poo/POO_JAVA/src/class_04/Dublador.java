package class_04;


public class Dublador {
    private String nome;
    private long registroProfissional;
    private Personagem personagemDublado;


    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public long getRegistroProfissional() {
        return registroProfissional;
    }

    public void setRegistroProfissional(long registroProfissional) {
        this.registroProfissional = registroProfissional;
    }

    public Personagem getPersonagemDublado() {
        return personagemDublado;
    }

    public void setPersonagemDublado(Personagem personagemDublado) {
        this.personagemDublado = personagemDublado;
    }
}
